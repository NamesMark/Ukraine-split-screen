	<section class="space-y-3">
		<h2 class="text-xl font-semibold">What is this?</h2>
		<p>
			Ukraine Before and After is a split-screen satellite imagery tool that shows the
			visible impact of russia's criminal war on Ukrainian cities. It displays two satellite views
			side by side — Google Maps and Bing Maps — so you can compare imagery from
			different providers and time periods at the same location. Bing Maps is pre-war,
			and Google Maps is more or less current. In 2023 it showed images of absolute devastation
			of <a class="anchor" href="https://en.wikipedia.org/wiki/Mariupol" target="_blank" rel="noreferrer">Mariupol</a>. The current, more recent images,
			despite attempts of the occupiers to hide their crimes, still show the scale of atrocities.
		</p>
		<p>
			The saved locations highlight sites of documented destruction: hospitals, theaters,
			residential areas, cemeteries, and military positions. Each one tells a story that
			can be seen from space.
			Please propose more locations to document these crimes, make screenshots, and share them online.
		</p>
	</section>

	<section class="space-y-3">
		<h2 class="text-xl font-semibold">How to use</h2>
		<ul class="list-disc list-inside space-y-2">
			<li><strong>Select a location</strong> from the sidebar to jump to a point of interest. Both maps pan and zoom together.</li>
			<li><strong>Show Labels</strong> toggles street and place names on the Google Maps panel.</li>
			<li><strong>Save Screenshot</strong> downloads the current satellite view as a PNG image.</li>
			<li><strong>Suggest Location</strong> lets you submit a new location for review. Navigate to a spot, click the button, and fill in a name and description. This creates a GitHub issue that the maintainers can review and add to the map.</li>
			<li><strong>Fullscreen</strong> hides the sidebar for a larger map view. Press <kbd class="kbd">Esc</kbd> to exit.</li>
		</ul>
